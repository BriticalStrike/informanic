        <h2>Learning Resources</h2>
        
        <p>I have had a significant amount of help learning programming, Excel, and analysis. I want to share some of the resources I have used for acquiring these skills and keeping up-to-date on the latest technologies in my area of learning.</p>
        <h3>Software</h3>
        <p>The following software is all open-source and free to use for a variety of projects. I have used all of them and will continue to do so as they provide great functionality and support for all types of media projects.</p>
        <ul>
            <li><a href="https://www.gimp.org/" target="_blank">Gimp</a> is an open-source photo editing software.</li>
            <li><a href="https://www.audacityteam.org/" target="_blank">Audacity</a> is a free audio and sound editing software I have used to record my podcast and various videos.</li>
            <li><a href="https://obsproject.com/" target="_blank">OBS Studio</a> or Open Broadcast Software Studio is an open-source recording software. It can be used to screen record as well as stream. It has wide functionality for recording.</li>
            <li><a href="https://discordapp.com/" target="_blank">Discord</a> is a free voice and text chat. While it was originally created for gaming, it has great functionality and includes screen-sharing, group chats, free server creation, and stream overlay. I have used this software for most of my group projects and it has worked wonderfully.</li>
        </ul>

        <h3>Youtube Channels</h3>

        <h4>Programming</h4>
        <ul>
            <li><a href="https://www.youtube.com/channel/UCCezIgC97PvUuR4_gbFUs5g" target="_blank">Corey Schafer</a> is a wonderful resource for programming, especially Python. He makes an effort to explain concepts in an easy-to-understand way.</li>
        </ul>
        <h4>Mathematics</h4>
        <ul>
            <li><a href="https://www.youtube.com/channel/UCFrjdcImgcQVyFbK04MBEhA" target="_blank">Brandon Foltz</a> is a great channel for learning about statistics to help with data anlysis.</li>
        </ul>
        <h3>Books</h3>
        <ul>
            <li><a href="https://automatetheboringstuff.com/" target="_blank">Automate the Boring Stuff</a> by Al Sweigart is free to read under a Creative Commons License. The entire book is available in-browser for learning the basics of Python and incorporating it into your everyday life.</li>
        </ul>

        <h3>Courses</h3>
        <p>While these courses are paid for on Udemy, they often have discounts or free courses available. These are courses I have taken that have helped me immensely.</p>
        
        <h4>Programming</h4>
        <ul>
            <li><a href="https://www.udemy.com/complete-python-bootcamp/" target="_blank">Complete Python Bootcamp</a> by Jose Portilla is a great introduction into Python. The course teaches everything from the basics of programming to OOP and different Python libraries.</li>
            <li><a href="https://www.udemy.com/the-python-mega-course/" target="_blank">The Python Mega Course</a> by Ardit Sulce dives head first into creating apps with Python. These are real-world projects from websites to face recognition that introduce you to a wide array of Python libraries.</li>
            <li><a href="https://www.udemy.com/the-ultimate-mysql-bootcamp-go-from-sql-beginner-to-expert/" target="_blank">The Ultimate MySQL Bootcamp</a> by Colt Steele introduces you to MySQL through the Cloud9 in-browser IDE.</li>
        </ul>
        <h4>Excel</h4>
        <ul>
            <li><a href="https://www.udemy.com/microsoft-excel-2013-from-beginner-to-advanced-and-beyond/" target="_blank">Microsoft Excel - Excel from Beginner to Advanced</a> by Kyle Pew is a great course for learning Excel. It is a three-part course that takes you from Excel basics to learning PivotTables, advanced functions, macros, and VBA.</li>
        </ul>

        <h3>Tutorial Websites</h3>
        <ul>
            <li><a href="https://pythonprogramming.net/" target="_blank">Pythonprogramming.net</a> contains a variety of tutorials for different Python projects. The authors usually include Youtube tutorials on their posts and walk you step-by-step through creating scripts and programming a variety of apps or bots.</li>
        </ul>
        <h3>Other Resources</h3>
        <p>I also want to include websites that keep me up-to-date with new technologies, allow me to ask questions and find answers, and support my efforts in general.</p>
        <ul>
            <li>Reddit a wonderful resource for all types of programming and Excel questions: <a href="https://www.reddit.com/r/learnprogramming" target="_blank">/r/Learnprogramming</a> for all programming languages, <a href="https://www.reddit.com/r/Python/" target="_blank">/r/Python</a> for news and posts about Python, <a href="https://www.reddit.com/r/webdev/" target="_blank">/r/WebDev</a> for web development news and posts, <a href="https://www.reddit.com/r/excel" target="_blank">/r/Excel</a> for learning Microsoft Excel and asking questions about difficult Excel issues.</li>
        </ul>
        <p>There are many more resources I have used in my journey, however, I have found these to be the most useful and the ones I find myself coming back to for learning.</p>

        
